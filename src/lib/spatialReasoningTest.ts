// Get random spatial reasoning questions for the test
export const getRandomSpatialReasoningQuestions = (count = 15) => {
  // Create a proper shuffle function using Fisher-Yates algorithm
  const shuffled = [...spatialReasoningQuestions];
  for (let i = shuffled.length - 1; i > 0; i--) {
    const j = Math.floor(Math.random() * (i + 1));
    [shuffled[i], shuffled[j]] = [shuffled[j], shuffled[i]];
  }
  
  // Select questions ensuring balanced difficulty distribution
  const easyQuestions = shuffled.filter(q => q.difficulty === "Easy").slice(0, 6);
  const mediumQuestions = shuffled.filter(q => q.difficulty === "Medium").slice(0, 6);
  const hardQuestions = shuffled.filter(q => q.difficulty === "Hard").slice(0, 3);
  
  const selectedQuestions = [...easyQuestions, ...mediumQuestions, ...hardQuestions];
  
  // Shuffle the selected questions again
  for (let i = selectedQuestions.length - 1; i > 0; i--) {
    const j = Math.floor(Math.random() * (i + 1));
    [selectedQuestions[i], selectedQuestions[j]] = [selectedQuestions[j], selectedQuestions[i]];
  }
  
  return selectedQuestions.map((question, index) => ({
    ...question,
    id: index + 1 // Reassign IDs for the selected questions
  }));
};

// Calculate spatial reasoning score based on answers
export const calculateSpatialReasoningScore = (answers: any, questions: any[]) => {
  let correctAnswers = 0;
  const totalQuestions = questions.length;
  
  questions.forEach((question: any) => {
    const userAnswer = answers[question.id];
    if (userAnswer !== undefined) {
      // Find the correct answer index after randomization
      const correctAnswerText = question.options[question.correct];
      if (userAnswer === correctAnswerText) {
        correctAnswers++;
      }
    }
  });
  
  const accuracy = Math.round(((correctAnswers / totalQuestions) * 100) * 10) / 10;
  
  // Determine spatial reasoning level
  let level = "";
  let description = "";
  
  if (accuracy >= 90) {
    level = "Visual Architect";
    description = "Exceptional spatial reasoning abilities with strong 3D visualization and mental rotation skills.";
  } else if (accuracy >= 80) {
    level = "Spatial Analyst";
    description = "Strong spatial reasoning with good pattern recognition and geometric understanding.";
  } else if (accuracy >= 70) {
    level = "Geometric Thinker";
    description = "Solid spatial reasoning skills with good understanding of shapes and spatial relationships.";
  } else if (accuracy >= 60) {
    level = "Developing Spatial";
    description = "Basic spatial reasoning skills with room for improvement in visualization and geometric thinking.";
  } else {
    level = "Needs Practice";
    description = "Spatial reasoning skills need development through practice and targeted exercises.";
  }
  
  // Calculate category scores
  const categoryScores: any = {};
  const categoryTotals: any = {};
  const categoryCorrect: any = {};
  
  questions.forEach((question: any) => {
    const category = question.category;
    if (!categoryScores[category]) {
      categoryScores[category] = 0;
      categoryTotals[category] = 0;
      categoryCorrect[category] = 0;
    }
    
    categoryTotals[category]++;
    const userAnswer = answers[question.id];
    if (userAnswer !== undefined) {
      const correctAnswerText = question.options[question.correct];
      if (userAnswer === correctAnswerText) {
        categoryCorrect[category]++;
      }
    }
  });
  
  // Calculate category percentages
  Object.keys(categoryScores).forEach(category => {
    categoryScores[category] = Math.round(((categoryCorrect[category] / categoryTotals[category]) * 100) * 10) / 10;
  });
  
  return {
    correctAnswers,
    totalQuestions,
    accuracy,
    level,
    description,
    categoryScores,
    answeredQuestions: Object.keys(answers).length
  };
};

